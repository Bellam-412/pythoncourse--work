print("Division(/):",a%b)     # Division(/): 10
print("Floor Division(//):",a//b)  #Floor Division(//): 0
print("Modulus(%):",a%b)          #Modulus(%): 10
print("Exponentiation(**):",a**b) #Exponentiation(**): 100000000000000000000


